-- Ctrip OTA metric tables.
-- Scope: Ctrip eBooking OTA channel data only; excludes biztravel modules for now.
-- This migration keeps online_daily_data compatible and stores new Ctrip fields in merged fact/entity tables.

CREATE TABLE IF NOT EXISTS `ota_ctrip_capture_runs` (
  `id` bigint unsigned NOT NULL AUTO_INCREMENT,
  `tenant_id` int unsigned DEFAULT NULL,
  `system_hotel_id` int unsigned DEFAULT NULL,
  `ota_hotel_id` varchar(64) NOT NULL DEFAULT '',
  `hotel_name` varchar(160) NOT NULL DEFAULT '',
  `profile_id` varchar(80) NOT NULL DEFAULT '',
  `data_date` date DEFAULT NULL,
  `source` varchar(20) NOT NULL DEFAULT 'ctrip',
  `capture_mode` varchar(40) NOT NULL DEFAULT 'browser_profile',
  `capture_sections` text NULL,
  `auth_status` varchar(30) NOT NULL DEFAULT '',
  `gate_status` varchar(30) NOT NULL DEFAULT '',
  `gap_status` varchar(30) NOT NULL DEFAULT '',
  `response_count` int unsigned NOT NULL DEFAULT 0,
  `fact_count` int unsigned NOT NULL DEFAULT 0,
  `standard_row_count` int unsigned NOT NULL DEFAULT 0,
  `evidence_file` varchar(255) NOT NULL DEFAULT '',
  `started_at` datetime DEFAULT NULL,
  `captured_at` datetime DEFAULT NULL,
  `finished_at` datetime DEFAULT NULL,
  `created_by` int unsigned DEFAULT NULL,
  `create_time` datetime DEFAULT CURRENT_TIMESTAMP,
  `update_time` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_ctrip_runs_evidence` (`evidence_file`),
  KEY `idx_ctrip_runs_tenant_date` (`tenant_id`, `data_date`),
  KEY `idx_ctrip_runs_hotel_date` (`system_hotel_id`, `data_date`),
  KEY `idx_ctrip_runs_status` (`source`, `gate_status`, `gap_status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Ctrip OTA capture run metadata';

CREATE TABLE IF NOT EXISTS `ota_ctrip_metric_catalog` (
  `id` bigint unsigned NOT NULL AUTO_INCREMENT,
  `metric_key` varchar(120) NOT NULL,
  `metric_label` varchar(160) NOT NULL DEFAULT '',
  `category` varchar(60) NOT NULL DEFAULT '',
  `metric_scope` varchar(40) NOT NULL DEFAULT 'ota_channel',
  `data_type` varchar(50) NOT NULL DEFAULT '',
  `unit` varchar(30) NOT NULL DEFAULT '',
  `value_type` varchar(30) NOT NULL DEFAULT '',
  `selection_status` varchar(30) NOT NULL DEFAULT 'selected',
  `build_decision` varchar(60) NOT NULL DEFAULT '',
  `target_table` varchar(80) NOT NULL DEFAULT '',
  `capture_status` varchar(60) NOT NULL DEFAULT '',
  `merge_group` varchar(80) NOT NULL DEFAULT '',
  `sections_json` text NULL,
  `endpoints_json` text NULL,
  `source_keys_json` text NULL,
  `is_common_dimension` tinyint NOT NULL DEFAULT 0,
  `is_biztravel` tinyint NOT NULL DEFAULT 0,
  `needs_user_site` tinyint NOT NULL DEFAULT 0,
  `notes` text NULL,
  `create_time` datetime DEFAULT CURRENT_TIMESTAMP,
  `update_time` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_ctrip_metric_catalog_key` (`metric_key`, `category`),
  KEY `idx_ctrip_metric_catalog_select` (`selection_status`, `build_decision`),
  KEY `idx_ctrip_metric_catalog_target` (`target_table`, `capture_status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Ctrip OTA field catalog and build selection';

