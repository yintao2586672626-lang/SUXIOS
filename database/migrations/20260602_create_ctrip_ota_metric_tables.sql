  KEY `idx_ctrip_metric_facts_section` (`capture_section`, `endpoint_id`, `metric_key`),
  KEY `idx_ctrip_metric_facts_entity` (`entity_type`, `entity_key`, `data_date`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Merged Ctrip OTA scalar metric facts';

